package com.example.demo.model;

public class Meetup {
	
	private int id;
	private String group_id;
	private String category;
	private String description;
	private int limitation;
	private int pax;
	private String release_openid;
	private String join_openid;
	private int is_delete;
	private String due_datetime;
	private String c_time;
	private String region;
	private String campus;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGroup_id() {
		return group_id;
	}
	public void setGroup_id(String group_id) {
		this.group_id = group_id;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getLimitation() {
		return limitation;
	}
	public void setLimitation(int limitation) {
		this.limitation = limitation;
	}
	public int getPax() {
		return pax;
	}
	public void setPax(int pax) {
		this.pax = pax;
	}
	public String getRelease_openid() {
		return release_openid;
	}
	public void setRelease_openid(String release_openid) {
		this.release_openid = release_openid;
	}
	public int getIs_delete() {
		return is_delete;
	}
	public void setIs_delete(int is_delete) {
		this.is_delete = is_delete;
	}
	public String getDue_datetime() {
		return due_datetime;
	}
	public void setDue_datetime(String due_datetime) {
		this.due_datetime = due_datetime;
	}
	public String getC_time() {
		return c_time;
	}
	public void setC_time(String c_time) {
		this.c_time = c_time;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getCampus() {
		return campus;
	}
	public void setCampus(String campus) {
		this.campus = campus;
	}
	public String getJoin_openid() {
		return join_openid;
	}
	public void setJoin_openid(String join_openid) {
		this.join_openid = join_openid;
	}
	
	
	
	
}
